package modelo;

public class Examen {
    
    private String nombreExmane;
    private float puntaje;

    public void setNombreExmane(String nombreExmane) {
        this.nombreExmane = nombreExmane;
    }

    public void setPuntaje(float puntaje) {
        this.puntaje = puntaje;
    }

    public String getNombreExmane() {
        return nombreExmane;
    }

    public float getPuntaje() {
        return puntaje;
    }

}
